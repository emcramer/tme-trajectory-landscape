# ================================================================
# SVG PARSING HELPERS
# ================================================================

def parse_svg_dimensions(filepath):
    """Return width/height in pt and the viewBox string."""
    with open(filepath, "r") as fh:
        header = fh.read(3000)

    w_m = re.search(r'width="([0-9.]+)(pt|mm|px|)"', header)
    h_m = re.search(r'height="([0-9.]+)(pt|mm|px|)"', header)
    w_val, w_unit = float(w_m.group(1)), (w_m.group(2) or "pt")
    h_val, h_unit = float(h_m.group(1)), (h_m.group(2) or "pt")

    width_pt  = w_val * MM_TO_PT if w_unit == "mm" else w_val
    height_pt = h_val * MM_TO_PT if h_unit == "mm" else h_val

    vb_m = re.search(r'viewBox="([^"]+)"', header)
    if vb_m:
        viewbox = vb_m.group(1)
    else:
        viewbox = f"0 0 {w_val} {h_val}"

    vb_parts = viewbox.split()
    return {
        "width_pt":  width_pt,
        "height_pt": height_pt,
        "viewbox":   viewbox,
        "vb_width":  float(vb_parts[2]),
        "vb_height": float(vb_parts[3]),
    }


def extract_svg_inner(filepath):
    """Return everything between the opening <svg …> and closing </svg>.

    Also strips metadata blocks to reduce file size.
    """
    with open(filepath, "r") as fh:
        raw = fh.read()
    # strip XML declaration + doctype
    raw = re.sub(r"<\?xml[^?]*\?>", "", raw)
    raw = re.sub(r"<!DOCTYPE[^>]*>", "", raw)
    m = re.search(r"<svg[^>]*>(.*)</svg>", raw, re.DOTALL)
    inner = m.group(1) if m else raw
    # strip <metadata> blocks (RDF, Dublin Core, etc.) to save space
    inner = re.sub(r"\s*<metadata>.*?</metadata>", "", inner, flags=re.DOTALL)
    # strip Inkscape/Sodipodi namespace-prefixed attributes (they lose their
    # declarations when the outer <svg> is removed, causing XML parse errors)
    inner = re.sub(r'\s+(?:inkscape|sodipodi):[a-zA-Z-]+="[^"]*"', "", inner)
    # strip <sodipodi:...> elements
    inner = re.sub(r"\s*<sodipodi:[^>]*/>", "", inner)
    inner = re.sub(r"\s*<sodipodi:[^>]*>.*?</sodipodi:[^>]*>", "", inner, flags=re.DOTALL)
    return inner


def prefix_ids(content, prefix):
    """Prefix every id / xlink:href / url(#…) to avoid cross-panel clashes."""
    content = re.sub(
        r'id="([^"]+)"',
        lambda m: f'id="{prefix}_{m.group(1)}"',
        content,
    )
    content = re.sub(
        r'xlink:href="#([^"]+)"',
        lambda m: f'xlink:href="#{prefix}_{m.group(1)}"',
        content,
    )
    content = re.sub(
        r"url\(#([^)]+)\)",
        lambda m: f"url(#{prefix}_{m.group(1)})",
        content,
    )
    return content

