#!/usr/bin/env python3
"""
================================================================================
FIGURE 2: ABM TME State Space
================================================================================

Generates Figure 2 from Cramer et al. (manuscript).
Composes individual figure panels (A–I) into a single publication-ready SVG.

PREREQUISITES — run these scripts first to generate the panel SVGs:
  Panel A  : code/02_abm_state_space_analysis.marimo.py
             → output/figures/abm/abm_state_space_spatial_statistics_clustermap.svg
  Panels B–H: code/01_abm_generate_embedding.py
             → output/figures/abm/effector_T_cell.svg
             → output/figures/abm/exhausted_T_cell.svg
             → output/figures/abm/malignant_epithelial_cell.svg
             → output/figures/abm/malignant_epithelial_cell_degree_centrality.svg
             → output/figures/abm/abm_time_umap.svg
             → output/figures/abm/abm_state_umap.svg
             → output/figures/abm/simulation_trajectories_over_umap/simulation_trajectories_n3.svg
  Panel I  : code/abm_state_image_examples.marimo.py
             (generates per-state snapshot PNGs; assemble into panel_i SVG
              manually in Inkscape, then save to
              output/figures/abm/abm_state_snapshots.svg)

Key features:
  - Parametric figure width/height for different journal requirements
  - Replaces matplotlib path-based text with editable Arial <text> elements
  - Panel labels (A, B, C …) in bold Arial
  - Harmonised font sizes: titles 12 pt, axis/legend labels 10 pt, ticks 9 pt
  - A multiplicative font_scale adjusts all sizes proportionally

Layout (default – 4 rows):
  Row 1 : Panel A          (full width – clustermap)
  Row 2 : Panels B, C, D, E (4 across – cell-type scatter/UMAP plots)
  Row 3 : Panels F, G, H    (3 across – UMAPs, trajectories)
  Row 4 : Panel I            (full width – state snapshots)

  Edit LAYOUT_ROWS near the top of this file to change the grid structure.

Usage (run from repo root):
  python figures/figure_2/generate_figure_2.py                       # defaults (190 mm)
  python figures/figure_2/generate_figure_2.py --width 180           # Nature width
  python figures/figure_2/generate_figure_2.py --font-scale 1.2      # 20 % larger fonts
  python figures/figure_2/generate_figure_2.py --width 174 --font-scale 0.9 --output figure_2.svg

Note on fonts:
  Panels B–H have their matplotlib path-based text REPLACED with editable
  <text> elements in Arial.  Panel A keeps its original glyph-path text
  (Lato, scale-adjusted).  To get fully editable text in Panel A, re-export
  the source plot with  plt.rcParams['svg.fonttype'] = 'none'.
"""

