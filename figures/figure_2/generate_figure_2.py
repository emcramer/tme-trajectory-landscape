def extract_text_groups(filepath):
    """Parse every <g id="text_N"> group in a matplotlib SVG.

    Returns a list of dicts with keys:
        id, content, fill, x, y, rotation, scale, text_width_pt
    """
    with open(filepath, "r") as fh:
        raw = fh.read()

    results = []
    header_pat = re.compile(
        r'<g id="(text_\d+)">\s*\n\s*<!--\s*(.+?)\s*-->\s*\n\s*<g([^>]*)>'
    )
    # also grab whole group for width estimation
    group_pat_template = r'<g id="{tid}">\s*<!--.*?-->\s*<g[^>]*>.*?</g>\s*</g>'

    for hm in header_pat.finditer(raw):
        tid = hm.group(1)
        text_content = hm.group(2)
        attrs = hm.group(3)

        fill_m = re.search(r"fill:\s*([^;\"]+)", attrs)
        fill = fill_m.group(1).strip() if fill_m else "#333333"

        transform_m = re.search(r'transform="([^"]+)"', attrs)
        transform = transform_m.group(1) if transform_m else ""

        tr_m = re.search(r"translate\(([0-9eE.+-]+)\s+([0-9eE.+-]+)\)", transform)
        tx = float(tr_m.group(1)) if tr_m else 0.0
        ty = float(tr_m.group(2)) if tr_m else 0.0

        rot_m = re.search(r"rotate\(([0-9eE.+-]+)\)", transform)
        rotation = float(rot_m.group(1)) if rot_m else 0.0

        sc_m = re.search(r"scale\(([0-9.]+)", transform)
        scale = float(sc_m.group(1)) if sc_m else 0.1

        # estimate glyph-based width
        gpat = group_pat_template.format(tid=re.escape(tid))
        gm = re.search(gpat, raw, re.DOTALL)
        max_offset = 0.0
        if gm:
            for um in re.finditer(r"translate\(([0-9.]+)\s", gm.group()):
                val = float(um.group(1))
                if val > max_offset:
                    max_offset = val
        text_width_pt = (max_offset + 58) * scale  # 58 ≈ avg last-char advance

        results.append({
            "id": tid,
            "content": text_content,
            "fill": fill,
            "x": tx,
            "y": ty,
            "rotation": rotation,
            "scale": scale,
            "text_width_pt": text_width_pt,
        })
    return results


def strip_matplotlib_text(inner_svg):
    """Remove all <g id='text_N'> groups (and their glyph defs) from inner SVG."""
    return re.sub(
        r"\s*<g id=\"text_\d+\">\s*<!--.*?-->\s*<g[^>]*>.*?</g>\s*</g>",
        "",
        inner_svg,
        flags=re.DOTALL,
    )


def classify_text_role(scale, all_scales):
    """Classify a text element as title / axis_label / tick_label."""
    uniq = sorted(set(all_scales), reverse=True)
    if len(uniq) >= 3:
        if scale >= uniq[0] - 0.002:
            return "title"
        if scale >= uniq[1] - 0.002:
            return "axis_label"
        return "tick_label"
    if len(uniq) == 2:
        return "title" if scale >= uniq[0] - 0.002 else "tick_label"
    return "tick_label"


def font_size_for_role(role, font_scale):
    sizes = {
        "title":      TITLE_FONT_SIZE_PT,
        "axis_label": AXIS_LABEL_FONT_SIZE_PT,
        "tick_label": TICK_LABEL_FONT_SIZE_PT,
    }
    return sizes.get(role, TICK_LABEL_FONT_SIZE_PT) * font_scale


def build_text_overlay(tg, px, py, panel_scale, font_scale, role,
                       vb_width=1.0, vb_height=1.0, panel_width_pt=0.0):
    """Return an SVG <text> element string positioned in composite coords.

    Uses the *exact* original translate(x, y) from the matplotlib SVG to
    position each replacement <text> element.  This ensures labels stay in
    their original locations and never drift into the plot area.

    Non-rotated text uses text-anchor="start" (matches matplotlib's glyph
    rendering which places characters left-to-right from the translate origin).
    Rotated text uses text-anchor="middle" so the label centres on its origin.

    Titles are auto-shrunk or truncated if they would overflow panel width.
    """
    fs = font_size_for_role(role, font_scale)
    # Map panel-local coordinates to composite coordinates
    cx = px + tg["x"] * panel_scale
    cy = py + tg["y"] * panel_scale
    css = role.replace("_", "-")

    # escape XML
    txt = tg["content"].replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")

    if tg["rotation"] != 0:
        # Rotated text (y-axis labels, colorbar labels) — centre on origin
        anchor = "middle"
        xform = f' transform="rotate({tg["rotation"]}, {cx:.2f}, {cy:.2f})"'
    else:
        anchor = "start"
        xform = ""

        # Title auto-shrink / truncation if it overflows panel width
        if role == "title" and panel_width_pt > 0:
            min_title_fs = 8.0 * font_scale
            est_width = len(txt) * fs * 0.55
            if est_width > panel_width_pt * 0.95:
                fs_try = panel_width_pt * 0.95 / (len(txt) * 0.55)
                if fs_try >= min_title_fs:
                    fs = fs_try
                else:
                    fs = min_title_fs
                    max_chars = int(panel_width_pt * 0.95 / (fs * 0.55)) - 1
                    if max_chars < len(txt):
                        txt = txt[:max_chars] + "…"

    return (
        f'<text x="{cx:.2f}" y="{cy:.2f}" class="{css}" '
        f'font-size="{fs:.1f}" text-anchor="{anchor}" '
        f'fill="{tg["fill"]}"{xform}>{txt}</text>'
    )

# ================================================================
# PANEL-A SPECIAL HANDLING  –  adjust scale factors in-place
# ================================================================

def adjust_panel_a_text_scales(inner_svg, panel_scale, font_scale):
    """Scale up title + axis-label text in panel A so they are legible.

    Clustermap row/column labels (scale≈0.08) are left at a moderate
    readable size because enlarging them would cause overlaps.
    """
    # target final pt for each role
    targets = {
        "title":      TITLE_FONT_SIZE_PT * font_scale,
        "axis_label": AXIS_LABEL_FONT_SIZE_PT * font_scale,
        "tick_label": 5.0 * font_scale,  # keep clustermap labels modest (smaller to avoid overlaps)
    }

    # mapping original scale → role (panel A specific)
    role_map = {
        0.14: "title",
        0.11: "axis_label",
        0.10: "tick_label",   # colorbar ticks
        0.08: "tick_label",   # clustermap labels
    }

    def _replace_scale(m):
        prefix = m.group(1)
        old_s  = float(m.group(2))
        suffix = m.group(3)

        # find closest role
        closest = min(role_map.keys(), key=lambda k: abs(k - old_s))
        role = role_map[closest]
        target_pt = targets[role]
        # new_scale such that new_scale * panel_scale * 72 ≈ target_pt
        new_s = target_pt / (72.0 * panel_scale)

        return f"{prefix}{new_s:.6f}{suffix}"

    # match  scale(S -S)  inside text-group inner <g> transforms
    # we only touch transforms that appear inside <g id="text_...">
    # Strategy: find each text group and adjust the scale inside it.
    def _adjust_group(gm):
        group = gm.group(0)
        group = re.sub(
            r"(scale\()([0-9.]+)(\s+-?[0-9.]+\))",
            _replace_scale,
            group,
        )
        return group

    return re.sub(
        r'<g id="text_\d+">\s*<!--.*?-->\s*<g[^>]*>.*?</g>\s*</g>',
        _adjust_group,
        inner_svg,
        flags=re.DOTALL,
    )

# ================================================================
# PANEL-I SPECIAL HANDLING  –  Inkscape SVG with <text> elements
# ================================================================

def adjust_panel_i_text(inner_svg, panel_scale, font_scale):
    """Set font-family to Arial and adjust font-size for panel I text."""
    # change font-family everywhere
    inner_svg = re.sub(
        r"font-family:[^;\"]+",
        f"font-family:{FONT_FAMILY}",
        inner_svg,
    )
    # Panel I text is legend labels at ~4.94px.  Map to tick_label size
    # compensated for panel scaling so final rendered size = target.
    target = TICK_LABEL_FONT_SIZE_PT * font_scale
    new_size_px = target / panel_scale  # px in viewBox coords
    inner_svg = re.sub(
        r"font-size:\s*[0-9.]+px",
        f"font-size:{new_size_px:.3f}px",
        inner_svg,
    )
    return inner_svg

# ================================================================
# LAYOUT COMPUTATION
# ================================================================

def compute_layout(panels_info, fig_w, gap, row_gap, label_offset):
    """Return {label: {x, y, width, height, scale, viewbox, …}} and total height."""
    layout = {}
    y = label_offset                       # start below first row label

    for row_labels in LAYOUT_ROWS:
        n = len(row_labels)
        avail = fig_w - (n - 1) * gap

        # equal-height formula: h = avail / Σ(w_i / h_i)
        aspect_sum = sum(
            panels_info[lb]["vb_width"] / panels_info[lb]["vb_height"]
            for lb in row_labels
        )
        row_h = avail / aspect_sum

        x = 0.0
        for lb in row_labels:
            info = panels_info[lb]
            pw = info["vb_width"] / info["vb_height"] * row_h
            sc = pw / info["vb_width"]
            layout[lb] = {
                "x": x, "y": y,
                "width": pw, "height": row_h,
                "scale": sc,
                "viewbox":   info["viewbox"],
                "vb_width":  info["vb_width"],
                "vb_height": info["vb_height"],
            }
            x += pw + gap

        y += row_h + row_gap + label_offset

    total_h = y - row_gap - label_offset
    return layout, total_h

# ================================================================
# COMPOSITE BUILDER
# ================================================================

def build_composite(script_dir, figure_width_mm, font_scale):
    fig_w  = figure_width_mm * MM_TO_PT
    gap    = PANEL_GAP_MM * MM_TO_PT
    rgap   = ROW_GAP_MM * MM_TO_PT
    lbl_off = PANEL_LABEL_OFFSET_MM * MM_TO_PT
    lbl_fs  = PANEL_LABEL_FONT_SIZE_PT * font_scale

    # ---- parse dimensions -------------------------------------------
    panels_info = {}
    for lb, fn in PANEL_FILES:
        panels_info[lb] = parse_svg_dimensions(os.path.join(script_dir, fn))

    layout, fig_h = compute_layout(panels_info, fig_w, gap, rgap, lbl_off)
    fig_h_mm = fig_h * PT_TO_MM

    # ---- SVG preamble -----------------------------------------------
    parts = [
        '<?xml version="1.0" encoding="UTF-8"?>',
        f'<svg xmlns="http://www.w3.org/2000/svg"',
        f'     xmlns:xlink="http://www.w3.org/1999/xlink"',
        f'     width="{figure_width_mm:.2f}mm"',
        f'     height="{fig_h_mm:.2f}mm"',
        f'     viewBox="0 0 {fig_w:.2f} {fig_h:.2f}">',
        f'  <style>',
        f'    .panel-label {{ font-family: {FONT_FAMILY}; font-weight: bold;',
        f'                    font-size: {lbl_fs:.1f}pt; fill: black; }}',
        f'    .title       {{ font-family: {FONT_FAMILY}; }}',
        f'    .axis-label  {{ font-family: {FONT_FAMILY}; }}',
        f'    .tick-label  {{ font-family: {FONT_FAMILY}; }}',
        f'  </style>',
        f'  <rect width="100%" height="100%" fill="white"/>',
    ]

    # ---- process each panel -----------------------------------------
    for lb, fn in PANEL_FILES:
        fp   = os.path.join(script_dir, fn)
        info = layout[lb]
        ps   = info["scale"]

        print(f"  Panel {lb}:  {info['width']*PT_TO_MM:.1f} × {info['height']*PT_TO_MM:.1f} mm   "
              f"scale={ps:.4f}   ({fn})")

        inner = extract_svg_inner(fp)
        text_overlays = []

        # --- text manipulation BEFORE id-prefixing -------------------
        # (regex patterns match original ids like "text_1", not "pB_text_1")

        # — Panel A: keep text, adjust scales in-place ----------------
        if lb == "A":
            inner = adjust_panel_a_text_scales(inner, ps, font_scale)

        # — Panels B–H: strip text, create Arial overlays ------------
        elif lb != "I":
            tgs = extract_text_groups(fp)
            all_scales = [t["scale"] for t in tgs]
            inner = strip_matplotlib_text(inner)
            for tg in tgs:
                role = classify_text_role(tg["scale"], all_scales)
                text_overlays.append(
                    build_text_overlay(
                        tg, info["x"], info["y"], ps, font_scale, role,
                        vb_width=info["vb_width"], vb_height=info["vb_height"],
                        panel_width_pt=info["width"],
                    )
                )

        # — Panel I: Inkscape SVG with <text> elements ----------------
        else:
            inner = adjust_panel_i_text(inner, ps, font_scale)

        # --- prefix IDs to avoid cross-panel clashes -----------------
        inner = prefix_ids(inner, f"p{lb}")

        # ---- nested <svg> for the panel ------------------------------
        parts.append(f"")
        parts.append(f'  <!-- ═══ Panel {lb} ═══ -->')
        parts.append(
            f'  <svg x="{info["x"]:.2f}" y="{info["y"]:.2f}"'
            f' width="{info["width"]:.2f}" height="{info["height"]:.2f}"'
            f' viewBox="{info["viewbox"]}"'
            f' xmlns:xlink="http://www.w3.org/1999/xlink">'
        )
        parts.append(inner)
        parts.append(f"  </svg>")

        # ---- text overlays (panels B–H) --------------------------------
        if text_overlays:
            parts.append(f"  <!-- Panel {lb} – text overlays -->")
            parts.append(f"  <g>")
            for ov in text_overlays:
                parts.append(f"    {ov}")
            parts.append(f"  </g>")

        # ---- panel label (bold letter) ------------------------------
        lx = info["x"] + 1
        ly = info["y"] - lbl_off * 0.25
        parts.append(
            f'  <text x="{lx:.2f}" y="{ly:.2f}" class="panel-label">{lb}</text>'
        )

    parts.append("</svg>")
    return "\n".join(parts)

# ================================================================
# MAIN
# ================================================================

def main():
    ap = argparse.ArgumentParser(
        description="Compose Figure 2 panels into a single publication-ready SVG."
    )
    ap.add_argument(
        "--width", type=float, default=DEFAULT_FIGURE_WIDTH_MM,
        help=f"Figure width in mm (default {DEFAULT_FIGURE_WIDTH_MM})"
    )
    ap.add_argument(
        "--font-scale", type=float, default=DEFAULT_FONT_SCALE,
        help=f"Multiplicative font-size factor (default {DEFAULT_FONT_SCALE})"
    )
    ap.add_argument(
        "--output", type=str, default="figure_2_composite.svg",
        help="Output filename (default figure_2_composite.svg)"
    )
    args = ap.parse_args()

    script_dir = os.path.dirname(os.path.abspath(__file__))

    print(f"Composing Figure 2  ({args.width:.0f} mm wide, font-scale {args.font_scale})")
    svg_text = build_composite(script_dir, args.width, args.font_scale)

    out_path = os.path.join(script_dir, args.output)
    with open(out_path, "w") as fh:
        fh.write(svg_text)

    size_mb = os.path.getsize(out_path) / 1e6
    print(f"\nSaved → {out_path}  ({size_mb:.1f} MB)")
    print(
        f"\nTip: to regenerate source panels with editable text, set\n"
        f"     plt.rcParams['svg.fonttype'] = 'none'  in your plotting code.\n"
        f"     This embeds <text> elements instead of glyph paths."
    )


if __name__ == "__main__":
    main()
