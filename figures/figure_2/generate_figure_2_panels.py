"""
generate_figure_2_panels.py — Standalone headless script for Figure 2 panels B–G

This script generates the UMAP-based panels of Figure 2 without requiring a
running marimo server. It loads pre-computed embeddings and clustered data
instead of re-running the expensive PCA → UMAP → kNN → Leiden → HAC pipeline.

Run from the repository root:
    python figures/figure_2/generate_figure_2_panels.py

Prerequisites
-------------
The following pre-computed files must exist before running this script:

    output/objects/abm/abm_full_features_pc_n647_umap_embedding.npy
        Pre-computed 2-D UMAP coordinates for every ABM time-step window.
        Generated by code/01_abm_generate_embedding.py (marimo notebook).

    data/abm/processed/abm_windows_clustered_with_state_label_20260212.csv
        Per-window feature averages, start/end time steps, and TME state labels.
        Generated by code/01_abm_generate_embedding.py (marimo notebook).

Outputs
-------
Panel B  output/figures/abm/feature_umaps/effector_T_cell.svg
Panel C  output/figures/abm/feature_umaps/exhausted_T_cell.svg
Panel D  output/figures/abm/feature_umaps/malignant_epithelial_cell.svg
Panel E  output/figures/abm/feature_umaps/malignant_epithelial_cell_degree_centrality.svg
Panel F  output/figures/abm/abm_time_umap.svg
Panel G  output/figures/abm/abm_state_umap.svg

Panel H (simulation trajectories) and Panel A (clustermap) are not generated
here — see code/01_abm_generate_embedding.py and
code/02_abm_state_space_analysis.marimo.py respectively.

Panel I (state snapshot montage) is assembled manually in Inkscape from images
generated by code/abm_state_image_examples.marimo.py.
"""

import os
import sys
import contextlib

import numpy as np
import pandas as pd
import matplotlib
matplotlib.use("Agg")  # headless backend — no display required
import matplotlib.pyplot as plt
import seaborn as sns

# ---------------------------------------------------------------------------
# Matplotlib style helpers
# ---------------------------------------------------------------------------

@contextlib.contextmanager
def safe_style(style_name):
    """Use a named matplotlib style; fall back to 'default' if not found."""
    try:
        with plt.style.context(style_name):
            yield
    except OSError:
        print(f"[warn] Style '{style_name}' not found — using 'default' style.")
        with plt.style.context("default"):
            yield


# ---------------------------------------------------------------------------
# Colour palette (matches 01_abm_generate_embedding.py)
# ---------------------------------------------------------------------------

THEME_PALETTE = sns.color_palette([
    "#1a535c", "#ee6c4d", "#84a98c", "#b8b8a8",
    "#6b4f7b", "#e6b800", "#4a6d86", "#c28e9e",
    "#4f7942", "#d4a88c",
])


# ---------------------------------------------------------------------------
# Legend helper
# ---------------------------------------------------------------------------

def enhance_legend_markers(ax=None, marker_size=10, alpha=1.0):
    """Increase the marker size and opacity of legend handles."""
    if ax is None:
        ax = plt.gca()
    legend = ax.get_legend()
    if legend is None:
        return
    for handle in legend.legend_handles:
        if hasattr(handle, "set_markersize"):
            handle.set_markersize(marker_size)
        if hasattr(handle, "set_alpha"):
            handle.set_alpha(alpha)


# ---------------------------------------------------------------------------
# Data loading
# ---------------------------------------------------------------------------

def load_data():
    """
    Load the pre-computed UMAP embedding and clustered window data.

    Returns
    -------
    embedding : ndarray, shape (n_windows, 2)
    df        : DataFrame with per-window features and 'state_label'
    """
    embedding_path = os.path.join(
        "output", "objects", "abm",
        "abm_full_features_pc_n647_umap_embedding.npy",
    )
    data_path = os.path.join(
        "data", "abm", "processed",
        "abm_windows_clustered_with_state_label_20260212.csv",
    )

    if not os.path.exists(embedding_path):
        sys.exit(
            f"[error] UMAP embedding not found: {embedding_path}\n"
            "Run code/01_abm_generate_embedding.py (marimo notebook) first."
        )
    if not os.path.exists(data_path):
        sys.exit(
            f"[error] Clustered data not found: {data_path}\n"
            "Run code/01_abm_generate_embedding.py (marimo notebook) first."
        )

    print(f"Loading UMAP embedding from {embedding_path} …")
    embedding = np.load(embedding_path)

    print(f"Loading clustered data from {data_path} …")
    df = pd.read_csv(data_path)

    print(f"  {embedding.shape[0]:,} windows loaded.")
    return embedding, df


# ---------------------------------------------------------------------------
# Panel generators
# ---------------------------------------------------------------------------

def save_panel(fig, path):
    """Save figure as SVG (and PNG for quick preview)."""
    os.makedirs(os.path.dirname(path), exist_ok=True)
    fig.savefig(path)
    png_path = path.replace(".svg", ".png")
    fig.savefig(png_path, dpi=150)
    print(f"  Saved {path}")


def plot_feature_over_umap(embedding, df, feature, output_dir, cmap="inferno"):
    """
    Panel B–E: colour each UMAP point by a single ABM spatial feature.

    Parameters
    ----------
    feature : str   column name in *df*
    """
    with safe_style("forest_and_sky_academic"):
        fig, ax = plt.subplots(figsize=(4, 4))

        scat = ax.scatter(
            embedding[:, 0],
            embedding[:, 1],
            c=df[feature],
            marker=".",
            alpha=0.6,
            s=5,
            cmap=cmap,
        )
        ax.set_xlabel("UMAP 1")
        ax.set_ylabel("UMAP 2")
        ax.set_title(f"ABM UMAP: {feature}")
        plt.colorbar(scat, ax=ax, label="Relative Population (%)")
        plt.tight_layout()

        out_path = os.path.join(output_dir, f"{feature}.svg")
        save_panel(fig, out_path)
        plt.close(fig)


def plot_time_umap(embedding, df, output_dir):
    """Panel F: colour UMAP points by average simulation time of the window."""
    # avg_time_per_timestep: midpoint of each 50-step window
    avg_time = (df["start_time_step"] + df["end_time_step"]) / 2

    with safe_style(os.path.expanduser("~/.matplotlib/stylelib/tme_research.mplstyle")):
        fig, ax = plt.subplots(figsize=(4, 4))

        scat = ax.scatter(
            embedding[:, 0],
            embedding[:, 1],
            c=avg_time,
            marker=".",
            alpha=0.6,
            s=5,
        )
        ax.set_xlabel("UMAP 1")
        ax.set_ylabel("UMAP 2")
        ax.set_title("Time-Delay Embedding")

        cbar = plt.colorbar(scat, ax=ax, label="Average Time of Window (Minutes)")
        cbar.set_ticks([avg_time.min(), avg_time.mean(), avg_time.max()])
        cbar.set_ticklabels(["Early", "Middle", "Late"])

        plt.tight_layout()
        out_path = os.path.join(output_dir, "abm_time_umap.svg")
        save_panel(fig, out_path)
        plt.close(fig)


def plot_state_umap(embedding, df, output_dir):
    """Panel G: colour UMAP points by TME state label."""
    plot_df = pd.DataFrame({
        "x": embedding[:, 0],
        "y": embedding[:, 1],
        "state_label": df["state_label"],
    })

    with safe_style(os.path.expanduser("~/.matplotlib/stylelib/tme_research.mplstyle")):
        fig, ax = plt.subplots(figsize=(4, 4))

        sns.scatterplot(
            plot_df,
            x="x",
            y="y",
            hue="state_label",
            palette=THEME_PALETTE,
            ax=ax,
            marker=".",
            s=5,
            alpha=0.6,
            edgecolor=None,
        )
        plt.legend(
            bbox_to_anchor=(1.02, 0.5),
            loc="center left",
            title="State",
        )
        enhance_legend_markers(ax=ax)
        ax.set_xlabel("UMAP 1")
        ax.set_ylabel("UMAP 2")
        ax.set_title("ABM Tissue States")
        plt.tight_layout()

        out_path = os.path.join(output_dir, "abm_state_umap.svg")
        save_panel(fig, out_path)
        plt.close(fig)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    plt.rcParams["svg.fonttype"] = "none"

    embedding, df = load_data()

    feature_umap_dir = os.path.join("output", "figures", "abm", "feature_umaps")
    abm_fig_dir      = os.path.join("output", "figures", "abm")

    os.makedirs(feature_umap_dir, exist_ok=True)
    os.makedirs(abm_fig_dir, exist_ok=True)

    # --- Panels B–E: feature UMAPs ---
    features = [
        "effector_T_cell",
        "exhausted_T_cell",
        "malignant_epithelial_cell",
        "malignant_epithelial_cell_degree_centrality",
    ]
    print("\n[Panel B–E] Feature UMAPs …")
    for feat in features:
        if feat not in df.columns:
            print(f"  [warn] Column '{feat}' not found in data — skipping.")
            continue
        print(f"  Plotting {feat} …")
        plot_feature_over_umap(embedding, df, feat, feature_umap_dir)

    # --- Panel F: time UMAP ---
    print("\n[Panel F] Time UMAP …")
    plot_time_umap(embedding, df, abm_fig_dir)

    # --- Panel G: state UMAP ---
    print("\n[Panel G] State UMAP …")
    plot_state_umap(embedding, df, abm_fig_dir)

    print("\nDone. Panels B–G generated successfully.")
    print("Panel A: run code/02_abm_state_space_analysis.marimo.py")
    print("Panel H: run code/01_abm_generate_embedding.py (marimo) — 'Simulation trajectories' cell")
    print("Panel I: run code/abm_state_image_examples.marimo.py and assemble in Inkscape")


if __name__ == "__main__":
    main()
